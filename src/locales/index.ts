const DICTS: Record<Locale, Dict> = { en, ru, zh, es };

/**
 * Returns a translation function bound to the current locale.
 * Usage in setup: `const t = useT(); t('swap.title')`.
 */
export function useT() {
  const settings = useSettingsStore();
  return computed(() => {
    const dict = DICTS[settings.locale as Locale] ?? en;
    return (key: string, fallback?: string) => dict[key] ?? en[key] ?? fallback ?? key;
  });
}

/** Plain-function translator for non-component contexts (e.g. toasts in stores). */
export function tStatic(key: string, locale: Locale = "en"): string {
  return DICTS[locale]?.[key] ?? en[key] ?? key;
}
